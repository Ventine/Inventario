    private void jButtonEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEliminarActionPerformed
        DefaultTableModel modelo;
        modelo = (DefaultTableModel) jTable.getModel();
           int a = jTable.getSelectedRow(); 
 
            if (a<0){ 
                JOptionPane.showMessageDialog(null, 
                "Debe seleccionar una fila de la tabla" ); 
         }else {
            int confirmar=JOptionPane.showConfirmDialog(null, 
           "Esta seguro que desea Eliminar el registro? "); 

                if(JOptionPane.OK_OPTION==confirmar) {
                   String codigo = (String) modelo.getValueAt(jTable.getSelectedRow(), 6);
                   int valor = (int) modelo.getValueAt(jTable.getSelectedRow(), 0);
                   long canti = Long.parseLong(codigo);
                    cantidad = cantidad - canti;
                    String s = Long.toString(cantidad);
                    ag.eliminarRegistro(valor);
                    jTextTotal.setText(s);
                   modelo.removeRow(a); 
                   JOptionPane.showMessageDialog(null,"Registro Eliminado" );
            } 
 
        }
            
    }//GEN-LAST:event_jButtonEliminarActionPerformed

