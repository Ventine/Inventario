    private void jButtonBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonBuscarActionPerformed

        long Cod = Long.parseLong(jTextcodigo.getText()) ;
        DatosSiesa DS = new DatosSiesa();
        String Des ="", Col ="", Ref ="", Tal ="";
        try {
            Des = DS.descVal(Cod);
            Col = DS.colorVal(Cod);
            Ref = DS.refVal(Cod);
            Tal = DS.talVal(Cod);
        } catch (IOException | ParseException ex) {
            Logger.getLogger(Vinventario.class.getName()).log(Level.SEVERE, null, ex);
        }
        jTextDes.setText(Des);
        jTextCol.setText(Col);
        jTextref.setText(Ref);
        jTextTal.setText(Tal);
    }//GEN-LAST:event_jButtonBuscarActionPerformed

